package com.aniket.entities;

public class Student 
{
	private Integer rno;
	private String name;
	private Double per;
	private Integer age;
	private String city;
	
	public Student(Integer rno, String name, Double per, Integer age, String city)
	{
		this.rno=rno;
		this.name=name;
		this.per=per;
		this.age=age;
		this.city=city;
	}
}

